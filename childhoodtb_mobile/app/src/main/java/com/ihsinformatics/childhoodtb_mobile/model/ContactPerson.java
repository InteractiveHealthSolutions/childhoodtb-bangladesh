public class ContactPerson {

    private String contactPerson;
    private int age;
    private String gender;
    private String symptoms;
    private String coughWeaks;
    private String fever;
    private String nightSweats;
    private String weightLoss;
    private String poorAppetite;
    private String refer;
    private String outcomeCode;
    private String remarks;

    public String getContactPerson() {
        return contactPerson;
    }

    public void setContactPerson(String contactPerson) {
        this.contactPerson = contactPerson;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getCoughWeaks() {
        return coughWeaks;
    }

    public void setCoughWeaks(String coughWeaks) {
        this.coughWeaks = coughWeaks;
    }

    public String getSymptoms() {
        return symptoms;
    }

    public void setSymptoms(String symptoms) {
        this.symptoms = symptoms;
    }

    public String getFever() {
        return fever;
    }

    public void setFever(String fever) {
        this.fever = fever;
    }

    public String getNightSweats() {
        return nightSweats;
    }

    public void setNightSweats(String nightSweats) {
        this.nightSweats = nightSweats;
    }

    public String getWeightLoss() {
        return weightLoss;
    }

    public void setWeightLoss(String weightLoss) {
        this.weightLoss = weightLoss;
    }

    public String getPoorAppetite() {
        return poorAppetite;
    }

    public void setPoorAppetite(String poorAppetite) {
        this.poorAppetite = poorAppetite;
    }

    public String getRefer() {
        return refer;
    }

    public void setRefer(String refer) {
        this.refer = refer;
    }

    public String getOutcomeCode() {
        return outcomeCode;
    }

    public void setOutcomeCode(String outcomeCode) {
        this.outcomeCode = outcomeCode;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }
}
